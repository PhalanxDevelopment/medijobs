                        <h3>Descoperă cum gestionează compania situațiile de conflict.</h3>
                        <p>
                            Situațiile de conflict apar și există peste tot, oricât am încerca să le evităm. Partea cu adevărat importantă este cum gestionezi conflictele atunci când apar. Ele nu trebuie evitate pentru că cele mai bune lecții pot fi extrase dintr-o astfel de situație. Evitarea unui conflict poate duce la frustrări și urmări mult mai dramatice decât recunoașterea și gestionarea lui.
                        </p>
                        <p>
                            Am identificat 4 întrebări care te pot ajuta să vezi cum este primită ideea de  conflict în companie:
                            <img src="http://cst-soft.com/medijobs/images/inline-image.png" style="float:right; margin:20px; margin-right: -112px;" alt="">
                        </p>
                        <p>1. Ce cauzează conflictul în companie și cum este rezolvat? </p>
                        <p>2. Cum sunt luate deciziile atunci când există diferențe mari de opinie? </p>
                        <p>3. Care este politica de feedback a companiei? </p>
                        <p>4. Care este ierarhia și cine este în măsură să ia deciziile în companie? </p>

                        <h3>Află cum este mediul de lucru și cum arată o zi de muncă în companie.</h3>
                        <p>În mod natural, îți dorești să lucrezi într-o companie care observă și apreciază performanța, celebrează succesul și căreia îi pasă 100% de angajații săi. Trebuie să îți dai seama încă de la interviu cât de flexibil este programul, dacă atitudinea la birou este una relaxată și funny și dacă, până la urmă, mediul de lucru se potrivește cu felul tău de a fi.</p>

                        <p>Ce întrebări poți pune pentru a afla mai multe detalii:</p>
                        <p>1. Care sunt modalitățile prin care sunt celebrate reușitele?</p>
                        <p>2. Cum este motivată echipa?</p>
                        <p>3. Ce fel de opțiuni flexibile de program există?</p>
                        <p>4. Există un program de bonusare în companie?</p>

                        <p>Ia în calcul faptul că fiecare companie are sistemul său de valori, propriile abordări cu privire la conflict, dar și mediul de lucru așa cum îl consideră productiv și potrivit. Bineînțeles că, realmente, poți afla detaliile doar dacă lucrezi acolo, însă seturile de întrebări de mai sus îți oferă seturile de întrebări de mai sus îți oferă un cadru potrivit de a descoperi mai multe perspective.</p>
